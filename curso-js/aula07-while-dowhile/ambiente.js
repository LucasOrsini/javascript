// while: estrutura de repetição com teste lógico no início
var c = 1
while (c <= 50){
    console.log(`Hello World!`)
    c++ // c = c + 1
}
  
// do while: estrutura de repetição com teste lógico no final
    do{
     console.log("Hello World!");
     c++ // c = c + 1
     } while (c <= 50)

// for: estrutura de repetição com variável de control (inicialização, teste lógico e incremento)
for (var c = 1; c <= 50; c++){
    console.log("Hello World!");
}




